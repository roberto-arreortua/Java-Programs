public class operadores {
    public static void main(String[] args){
        
        System.out.println("10 == 10.0 = "+(10 == 10.0));
        System.out.println("10 == 8.00 = "+(10 == 8.00));
        
        System.out.println("10 >  8.00 = "+(10 > 8.00));
        System.out.println("10 <  8.00 = "+(10 < 8.00));
        
        System.out.println("10 != 8.00 = "+(10 != 8.00));
        System.out.println("10 != 10.0 = "+(10 != 10.0));
        
         
        
    }
    
}
